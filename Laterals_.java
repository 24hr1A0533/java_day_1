public class Laterals_ {
    public static void main (String[]args){
        int a=100;//decimal leteral
        int b=013; //octa lateral
        int c=0x12;// hexa lateral
        int d=0b01; // binary lateral
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        float f=1.3333333f; //f is lateral after dot 7
        double aa=1.28328293d;//d is lateral after dot 15
        System.out.println(f);
        System.out.println(aa);
        boolean k=true; // true is lateral
        boolean g=false;// false is lateral
        System.out.println(f);
        System.out.println(g);
    }
}
